<?php

//$helloという変数に”Hello, “を
//$name という変数にあなたの名前を
//$world という変数に”‘s World!” を代入して、
//各変数を連結させて “Hello, あなたの名前’s World!” と表示してみましょう。

//$helloにHello,を代入
$hello = "Hello, ";
//nameにIkuoを代入
$name = "Ikuo";
//worldに's World!を代入
$world = "’s World!";

//$hello $name $worldを並べて表示.で文字を連結
echo $hello.$name.$world;
 ?>
